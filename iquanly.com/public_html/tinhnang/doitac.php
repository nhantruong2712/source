 
        <h1 style="text-align: center;">Đối tác</h1>
        <p style="text-align: center;"><em>Nhà cung cấp, Khách hàng</em></p>
         
        <h4><strong>1. Nhà cung cấp</strong></h4>
        <p style="text-align: justify;">Để sử dụng chức năng này, bạn vào mục quản lý hàng hóa ở menu "Đối tác" trên menu chính, rồi bạn click vào submenu "Nhà cung cấp" để bắt đầu quản lý hàng hóa</p>
        <p>
            Chức năng chính :
            </p><ol>
                <li>Thêm Nhà cung cấp</li>
				<li>Sửa, xóa Nhà cung cấp</li> 
			</ol>
        <p></p>
   
        <h5><strong>1.1. Thêm Nhà cung cấp</strong></h5>
        <p style="text-align: justify;">Ngay màn hình chính của menu "Đối tác"->"Nhà cung cấp", bạn nhấp vào nút Nhà cung cấp màu xanh</p>
       
        <p>
            Các Thông tin Nhà cung cấp bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Mã Nhà cung cấp </span>: Các mã hàng hóa không trùng nhau, ô này có thể bỏ trống thì phần mềm sẽ tự tạo mã Nhà cung cấp</li>
                <li><span style="color:#428bca">Tên Nhà cung cấp </span>:  Ví dụ: Hồng Hà</li>
				<li><span style="color:#428bca">Điện thoại </span>:  Số điện thoại của Nhà cung cấp</li>
                <li>
                    <span style="color:#428bca">Địa chỉ </span>: Địa chỉ của nhà cung cấp
                </li>
			</ol>
        <p>
            Ở phần Thêm thông tin có 3 mục là :
            </p><ol>
				<li><span style="color:#428bca">Email </span>: Địa chỉ hộp thư của Nhà cung cấp</li>
                <li><span style="color:#428bca">Mã số thuế </span>: Mã số thuế của Nhà cung cấp</li>
                <li><span style="color:#428bca">Công ty </span>: tên công ty của Nhà cung cấp</li>
<li><span style="color:#428bca">Ghi chú </span></li>				
			</ol>   
        <p>Xong mọi việc bạn nhấn nút "Lưu" để lưu thêm hàng hóa</p>    
        
        
        
        <h5><strong>1.2. Sửa và xóa Nhà cung cấp</strong></h5>
        <p style="text-align: justify;">Ở danh sách Nhà cung cấp bạn nhấn vào 1 dòng Nhà chung cấp sẽ hiển thị chi tiết Nhà cung cấp đó, 
        để sửa sản phẩm đó bạn nhấn nút "Cập nhật" màu xanh, để xóa sản phẩm này thì nhấn nút "Xóa" màu đỏ</p>
	
	<h4><strong>2. Khách hàng</strong></h4>
        <p style="text-align: justify;">Để sử dụng chức năng này, bạn vào mục quản lý hàng hóa ở menu "Đối tác" trên menu chính, rồi bạn click vào submenu "Nhà cung cấp" để bắt đầu quản lý hàng hóa</p>
        <p>
            Chức năng chính :
            </p><ol>
                <li>Thêm Khách hàng</li>
				<li>Sửa, xóa Khách hàng</li> 
                 
			</ol>
