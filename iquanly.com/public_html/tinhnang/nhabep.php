 
        <h1 style="text-align: center;">Nhà bếp</h1>
		 <p style="text-align: justify;">Bạn kích váo nút Nhà bếp để đến với chức năng Nhà bếp</p>
       <h4><strong>1. CHờ chế biến </strong></h4>
        <p style="text-align: justify;">Khi bộ phận thu nhân chọn xong các món ăn, dịch vụ rồi kích nút thông báo nhà bếp thì các thông tin đó năm ở danh mục chờ chế biên</p>
		
         <p style="text-align: justify;">Sau khi chế biến xong món bào thì bạn kích nút > màu trắng để chuyển sang chế độ món ăn đã xong chờ cung ứng</p>
        <h4><strong>2. CHế biến xong/ cung ứng</strong></h4>
        <p style="text-align: justify;">Sau khi các món ăn cung ứng xong và chuyển cho bên thu ngân chuyển cho khách hàng thì kích nút đã xong tất cả để kết thúc quy trình nhà bếp </p>
        