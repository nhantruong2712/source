 
        <h1 style="text-align: center;">Tính năng thiết lập</h1>
        <p style="text-align: center;"><em>Thiết lập cơ bản của cửa hàng trước khi sử dụng.</em></p>
         
        <h4><strong>1. Thiết lập cửa hàng</strong></h4>
        <p style="text-align: justify;">Trước khi sử dụng chính thức phần mềm, bạn nên vào mục thiết lập cửa hàng ở menu "Thiết lập", Ở bên tay phải phía trên màn hình. Rồi bạn click vào submenu "Thiết lập cửa hàng" để bắt đầu cài đặt một số các thông số cơ bản</p>
        <p>
            Chức năng chính :
            </p><ol>
				<li>Thiết lập Thông tin cửa hàng</li>
                <li>Thiết lập tính năng</li>
				 
			</ol>
        <p></p>

        <p>
            Thiết lập Thông tin cửa hàng bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Tên cửa hàng </span>: Tên cửa hàng của bạn, ví dụ: Nhà hàng Bếp Việt</li>
                <li><span style="color:#428bca">Địa chỉ </span>:  Là địa chỉ cửa hàng của bạn, ví dụ: 02 Hàm Nghi</li>
				<li><span style="color:#428bca">Khu vực </span>: Khu vực cửa hàng của bạn (có chức năng autocomplete để bạn dễ truy cứu khu vực), ví dụ: Bạc Liêu - Huyện Đông Hải</li>
                <li>
                    <span style="color:#428bca">Điện thoại </span>: Số điện thoại cửa hàng của bạn ví dụ: 0916863333
                </li>
                <li>
                    <span style="color:#428bca">Logo </span>: Bạn có thể tự upload logo cửa hàng, hoặc mặc định là logo của QUANLYTOT
                </li>
			</ol>
        <p>
            Thiết lập tính năng bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Sử dụng ngày hết hạn sản phẩm </span>: Tính năng sử dụng ngày hết hạn sản phẩm có thể được áp dụng cho nhà thuốc</li>
                 
			</ol>    
        <p>Xong mọi việc bạn nhấn nút "Lưu" để lưu thiết lập</p>
        <p style="text-align: center;"><img class="aligncenter size-full wp-image-9233" src="/Images/danh_sach_hop_dong_cam_do.png" alt="Thiết lập cửa hàng"></p>

         
        <h4><strong>2. Quản lý chi nhánh</strong></h4>
        <p style="text-align: justify;">Để sử dụng chức năng này bạn vào menu "Thiết lập", Ở bên tay phải phía trên màn hình. Rồi bạn click vào submenu "Quản lý chi nhánh" để có thể thêm bớt các chi nhánh kinh doanh của bạn</p>
        <p style="text-align: justify;">Tại đây bạn có thể thêm sửa xóa các chi nhánh của bạn một cách nhanh chóng</p>
        <p style="text-align: justify;">Khi vào màn hình chính của quản lý chi nhánh bạn sẽ thấy danh sách các chi nhánh, ấn vào 1 dòng sẽ hiển thị thông tin chi tiết của chi nhánh đó bao gồm cả danh sách người dùng (nhân viên) trong chi nhánh đó.</p>
        <p style="text-align: justify;">Để sửa chi nhánh bạn ấn nút "Cập nhật".</p>
        <p style="text-align: justify;">Bạn sẽ sửa các thông tin chi nhánh gồm: <span style="color:#428bca">Tên chi nhánh </span>,<span style="color:#428bca">Điện thoại </span>,<span style="color:#428bca">Email </span>,<span style="color:#428bca">Địa chỉ </span> </p>
        <p style="text-align: justify;">Để thêm chi nhánh bạn ấn nút "Chi nhánh" màu xanh.</p>
        <p style="text-align: justify;">Để xóa chi nhánh bạn ấn nút "Xóa" màu đỏ.</p>
        
        <p style="text-align: center;"><img class="aligncenter size-full wp-image-9233" src="/Images/tao_moi_hop_dong_cam_do.png" alt="Quản lý chi nhánh"></p>

         
        <h4><strong>3. Quản lý người dùng (nhân viên)</strong></h4>
        <p style="text-align: justify;">Để sử dụng chức năng này bạn vào menu "Thiết lập", Ở bên tay phải phía trên màn hình. Rồi bạn click vào submenu "Quản lý người dùng" để có thể thêm bớt các người dùng (nhân viên) của bạn</p>
        <p style="text-align: justify;">Chức năng chính trong phần Quản lý người dùng : </p>
        <ol>
                <li><span style="color:#428bca">Danh sách người dùng </span></li>
				<li><span style="color:#428bca">Thêm người dùng </span></li>
                <li><span style="color:#428bca">Sửa người dùng</span></li>
                <li><span style="color:#428bca">Cho phép/Ngừng hoạt động người dùng</span></li>
                <li><span style="color:#428bca">Phân quyền người dùng </span>: phân quyền các nhiệm vụ người dùng có thể thực hiện đc ở các chi nhánh</li>
                <li><span style="color:#428bca">Thêm vai trò người dùng</span>: các vai trò như: quản lý chi nhánh, nhân viên thu ngân, nhân viên bếp, nhân viên kho</li>
                <li><span style="color:#428bca">Sửa vai trò người dùng </span></li>
                <li><span style="color:#428bca">Xóa vai trò người dùng </span></li>
 
        </ol>
        <h5><strong>3.1 Danh sách người dùng</strong></h5> 
        <p style="text-align: justify;">Màn hình chính của phần quản lý người dùng là danh sách các người dùng hiện tại của chi nhánh.</p>
        <p style="text-align: justify;">Nhấn vào một dòng sẽ hiển thị thông tin chi tiết của người dùng đó.</p>
        <h5><strong>3.2 Thêm người dùng</strong></h5> 
        <p style="text-align: justify;">Để thêm người dùng bạn ấn nút "Người dùng" màu xanh.</p>
        <p style="text-align: justify;">Các thông tin người dùng gồm: </p>
        <ul>
				<li><span style="color:#428bca">Tên người dùng </span>: Họ và tên người dùng, ví dụ: Nguyễn Văn Nam</li>
                <li><span style="color:#428bca">Tên đăng nhập </span>: tên đăng nhập vào website quản lý của QLT, chỉ bao gồm chữ không dấu, không có khoảng trống ví dụ: nguyenvannam</li>
                <li><span style="color:#428bca">Mật khẩu </span>: mật khẩu đăng nhập</li>
                <li><span style="color:#428bca">Nhập lại mật khẩu</span>: phải trùng với mật khẩu ở trên</li>
                <li><span style="color:#428bca">Admin </span>: là quản trị tối cao hay không</li>
                <li><span style="color:#428bca">Phân quyền </span>: gán các vai trò vào người dùng này, ví dụ vai trò Quản trị chi nhánh</li>
                <li><span style="color:#428bca">Chi nhánh </span>: chi nhánh cấp quyền trên cho người dùng này, có thể chọn nhiều chi nhánh một lúc</li>
 
        </ul>
        <p style="text-align: justify;">Khi nhấn vào "Thông tin thêm" sẽ hiển thị thêm các thông tin sau:</p>
        <ul>
				<li><span style="color:#428bca">Email </span> </li>
                <li><span style="color:#428bca">Điện thoại </span> </li>
                <li><span style="color:#428bca">Địa chỉ </span> </li>
                <li><span style="color:#428bca">Ngày sinh</span> </li>
                 
        </ul>
        <h5><strong>3.3 Sửa người dùng</strong></h5> 
        <p style="text-align: justify;">Để sửa người dùng thì bạn nhấn vào người dùng đó ở bảng danh sách người dùng rồi ấn nút "Cập nhật" màu xanh.</p>
        
        <h5><strong>3.4 Cho phép/Ngừng hoạt động người dùng</strong></h5> 
        <p style="text-align: justify;">Để Cho phép/Ngừng hoạt động người dùng thì bạn nhấn vào người dùng đó ở bảng danh sách người dùng rồi ấn nút "Cho phép hoạt động" màu xanh hoặc "Ngừng hoạt động" màu đỏ.</p>
        
        <h5><strong>3.5 Phân quyền người dùng</strong></h5> 
        <p style="text-align: justify;">Để phân quyền người dùng thì bạn nhấn vào người dùng đó ở bảng danh sách người dùng rồi ấn vào tab "Phân Quyền", ở đây bạn có thể gán vai trò cho người dùng này vào các chi nhánh.</p>
        <p style="text-align: justify;">Ngoài ra ở đây bạn có thể thêm sửa xóa các vai trò người dùng</p>
