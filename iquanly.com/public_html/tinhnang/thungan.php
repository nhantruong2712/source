 
        <h1 style="text-align: center;">Thu nhân</h1>
        <p style="text-align: center;"><em>Để vào chức năng thu ngân bạn kích vào Thu ngân</em></p>
         
        <h4><strong>1. Tìm kiếm hàng hóa</strong></h4>
		 <p style="text-align: justify;"> Tìm kiếm theo mã hàng, tên hàng hoặc dùng máy quét mã vạch để tìm kiếm</p>
 <p style="text-align: justify;">  Lựa chọn trực tiếp từ danh sách sản phẩm kèm hình ảnh</p>
     <p style="text-align: justify;"> Hiển thị danh sách sản phẩm trong từng nhóm hàng</p>
  
    
		
		<h4><strong>2. Thông tin sản phẩm</strong></h4>  
		<p style="text-align: justify;">Bạn có thể thay đổi số lượng sản phẩm trong phiếu đặt hàng, hoặc giảm giá trên từng sản phẩm khi tạo phiếu đặt hàng.</p>
		 <p style="text-align: justify;">Bạn có thể thay đổi giá sản phẩm, % giảm giá</p>
		 
     		<h4><strong>3. Mở rộng </strong></h4>  
		<p style="text-align: justify;">Hệ thống hỗ trợ tìm kiếm thông tin khách hàng theo mã, tên khách hàng hoặc số điện thoại, hệ thống sẽ tự động hiển thị những tên giống như bạn gõ để lựa chọn.</p>
		 <p style="text-align: justify;">Khi khách hàng không có trong hệ thống, bạn cũng có thể thêm ngay trong màn hình Bán hàng bằng cách nhấn nút dấu + cạnh ô tìm kiếm khách hàng.</p>
        	<h4><strong>4. Ghi chú </strong></h4>  
		<p style="text-align: justify;">Bạn có thể nhập số % giảm giá của đơn hàng</p>
		 <p style="text-align: justify;"> Bạn nhập tổng số tiền khách trả thì thông tin tiền thừa khách trả sẽ được hiện trên màn hinh</p>
        
        <p>Xong mọi việc bạn nhấn nút thanh toán</p>    
        
        
        

	
	