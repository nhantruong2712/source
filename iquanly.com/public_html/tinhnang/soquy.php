 
        <h1 style="text-align: center;">Sổ quỹ</h1>
        <p style="text-align: center;"><em>Quản lý sổ quỹ</em></p>
         
        <h4><strong>1. Phiếu thu</strong></h4>

        <p>
            Chức năng chính :
            </p><ol>
                <li>Lập phiếu thu</li>
				<li>sửa, xóa, in phiếu thu</li> 
			</ol>
        <p></p>
   
        <h5><strong>1.1. Lập Phiếu thu</strong></h5>
		 <p style="text-align: justify;"> Ngay màn hình chính của menu "Sổ quỹ", bạn nhấp vào nút Lập phiếu thu màu xanh</p>
     
        <p>
            Các Thông tin Lập phiếu thu bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Mã Nhà phiếu </span>: Các mã phiếu không trùng nhau, ô này có thể bỏ trống thì phần mềm sẽ tự tạo mã phiếu</li>
                <li><span style="color:#428bca">Thời gian </span>:  </li>
				<li><span style="color:#428bca">Loại thu </span>: Loại phiếu thu, nếu chưa có thì bạn kích vào nút dấu công một bên thì bạn có thể thêm loại phiếu thu</li>
				<li><span style="color:#428bca">Nhóm người nộp </span>: Nhân viên, khách hàng, nhà cung cấp, khác </li>
				<li><span style="color:#428bca">Tên khách hàng </span>: Ghi họ tên của những người trong nhóm khách hàng, Nhà cung cấp, hoặc nhân viên; nếu chưa có thì kích vào nút dấu + một bên để thêm</li>
                <li>
                    <span style="color:#428bca">Giá trị </span>
                </li>
				<li>
                    <span style="color:#428bca">Ghi chú </span>
                </li>
			</ol>
        
        <p>Xong mọi việc bạn nhấn nút "Lưu" để lưu thêm phiếu thu</p>    
        
        
        
        <h5><strong>1.2. Sửa và xóa Phiếu thu</strong></h5>
        <p style="text-align: justify;">Ở danh sách phiếu bạn nhấn vào 1 dòng phiếu sẽ hiển thị chi tiết Nhà cung cấp đó và kích nút mở phiếu để sửa xóa hoặc kích nút in để in ấn, 
        để sửa phiếu đó bạn nhấn nút "Lưu" màu xanh, để xóa phiếu này thì nhấn nút "hủy bỏ" màu đỏ</p>
		
