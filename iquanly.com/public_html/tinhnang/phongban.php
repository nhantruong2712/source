
        <h1 style="text-align: center;">Phòng, bàn</h1>
        <p style="text-align: center;"><em>Phòng khách sạn, phòng karaoke, bàn bi-a, bàn Nhà hàng, bàn bóng bàn......</em></p>
         
        <h4><strong>1. Phòng, bàn</strong></h4>
        <p style="text-align: justify;">Để sử dụng chức năng này, bạn vào mục quản lý hàng hóa ở menu "Phòng bàn" trên menu chính" để bắt đầu quản lý phòng bàn</p>
        <p>
            Chức năng chính :
            </p><ol>
                <li>Thêm phòng bàn</li>
				<li>Sửa, xóa Phòng, bàn</li> 
			</ol>
        <p></p>
   
        <h5><strong>1.1. Thêm Phòng bàn</strong></h5>
        <p style="text-align: justify;">Ngay màn hình chính của menu "Phòng bàn", bạn nhấp vào nút Phòng bàn màu xanh</p>
       
        <p>
            Các Thông tin Phòng bàn bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Tên phòng bàm </span>: </li>
				<li><span style="color:#428bca">Ghi chú </span>:</li>
                <li>
                    <span style="color:#428bca">Nhóm </span>: Bàn bi-a, bàn bóng bàn, phòng khách sạn, phòng karaoke, bàn Nhà hàng.....
                </li>
			</ol>
           
        <p>Xong mọi việc bạn nhấn nút "Lưu" để lưu thêm phòng bàn</p>    
        
        
        
        <h5><strong>1.2. Sửa và xóa Phòng bàn</strong></h5>
        <p style="text-align: justify;">Ở danh sách Phòng bàn bạn nhấn vào 1 dòng Nhà chung cấp sẽ hiển thị chi tiết Phòng bàn đó, 
        để sửa sản phẩm đó bạn nhấn nút "Cập nhật" màu xanh, để xóa sản phẩm này thì nhấn nút "Xóa" màu đỏ</p>