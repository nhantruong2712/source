 
        <h1 style="text-align: center;">Quản lý hàng hóa</h1>
        <p style="text-align: center;"><em>Quản lý chủng loại hàng hóa, các hàng hóa, dịch vụ ..., quản lý kho</em></p>
         
        <h4><strong>1. Quản lý hàng hóa</strong></h4>
        <p style="text-align: justify;">Để sử dụng chức năng này, bạn vào mục quản lý hàng hóa ở menu "Hàng hóa" trên menu chính, rồi bạn click vào submenu "Danh mục" để bắt đầu quản lý hàng hóa</p>
        <p>
            Chức năng chính :
            </p><ol>
				<li>Danh sách nhóm hàng: nằm ngay menu trái</li>
                <li>Thêm nhóm hàng</li>
				<li>Sửa, xóa nhóm hàng</li> 
                <li>Danh sách hàng hóa: nằm ngay màn hình chính của menu "Hàng hóa"->"Danh mục"</li>
                <li>Thêm hàng hóa, dịch vụ, combo: dịch vụ có thể là Giờ hát, giờ chơi bi-a..., combo áp dụng với các sản phẩm có định lượng như các món ăn</li>
				<li>Sửa, xóa, ngừng/cho phép kinh doanh hàng hóa, dịch vụ, combo</li> 
                <li>Chuyển đổi đơn vị: khi hàng hóa có nhiều đơn vị như gói, cây ...</li> 
			</ol>
        <p></p>
        <p style="text-align: justify;">Khi vào submenu "Danh mục" bạn sẽ thấy hiển thị danh sách các hàng hóa (dịch vụ, combo)</p>
        <p style="text-align: justify;">Ở menu trái có thể quản lý các nhóm hàng hóa như thêm, sửa, xóa</p>
        <p style="text-align: justify;">Khi click vào 1 dòng sản phẩm thì sẽ hiển thị thông tin chi tiết của sản phẩm đó, tại đây có thể xóa hàng hóa, ngừng kinh doanh hàng hóa hoặc sửa hàng hóa này</p>
        <h5><strong>1.1. Thêm nhóm hàng</strong></h5>
        <p style="text-align: justify;">Ở menu trái bạn nhấn vào nút dấu cộng (+) một bên chữ "Nhóm hàng"</p>
        <h5><strong>1.2. Sửa và xóa nhóm hàng</strong></h5>
        <p style="text-align: justify;">Ở menu trái ở mục "Nhóm hàng": rê chuột qua một nhóm hàng bất kỳ sẽ hiện ra nút sửa có hình cái bút, bạn nhấn vào đó sẽ vào chức năng sửa hoặc xóa nhóm hàng đó</p>
        <h5><strong>1.3. Thêm hàng hóa</strong></h5>
        <p style="text-align: justify;">Ngay màn hình chính của menu "Hàng hóa"->"Danh mục", bạn nhấn vào nút "Thêm mới" màu xanh, sẽ có 3 lựa chọn cho bạn là:</p>
        <ol>
			<li><span style="color:#428bca">Thêm hàng hóa </span>: Hàng hóa thông thường kể cả món ăn nếu bạn không dùng chức năng định lượng</li>
            <li><span style="color:#428bca">Thêm dịch vụ </span>: Các dịch vụ như Giờ hát</li>
			<li><span style="color:#428bca">Thêm combo-đóng gói </span>: Các sản phẩm có thành phần ví dụ Món ăn</li>
             
		</ol>
        <h6><strong>1.3.1 Thêm hàng hóa</strong></h6>
        <p>
            Các Thông tin hàng hóa bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Mã hàng hóa </span>: Các mã hàng hóa không trùng nhau, có thể dùng mã vạch, ô này có thể bỏ trống thì phần mềm sẽ tự tạo mã hàng hóa</li>
                <li><span style="color:#428bca">Tên hàng hóa </span>:  Ví dụ: Bia Heiniken</li>
				<li><span style="color:#428bca">Nhóm hàng </span>: Nhóm hàng của hàng hóa này, ví dụ: Nguyên vật liệu. Tại đây bạn có thể thêm nhóm hàng bằng cách nhấn vào nút cộng (+)</li>
                <li>
                    <span style="color:#428bca">Giá bán </span>: Giá bán của hàng hóa này, đơn vị VNĐ
                </li>
                <li>
                    <span style="color:#428bca">Giá vốn </span>: Giá vốn của hàng hóa này, đơn vị VNĐ
                </li>
                <li><span style="color:#428bca">Tồn kho </span>: Tồn kho hiện tại của sản phẩm này, khi số lượng này khác 0 thì hệ thống sẽ tạo một phiếu kiểm kho cho sản phẩm này</li>
                <li>
                    <span style="color:#428bca">Ngày hết hạn </span>: Chỉ hiển thị khi bạn thiết lập chức năng "Sử dụng ngày hết hạn sản phẩm"
                </li>
                <li><span style="color:#428bca">Lựa chọn khác </span>: "Được bán trực tiếp": sản phẩm sẽ xuất hiện trên màn hình bán hàng ở chức năng <a href="/tinh-nang.php?t=thungan">Thu ngân</a></li>
			</ol>
        <p>
            Ở phần thông tin mở rộng có 3 mục là :
            </p><ol>
				<li><span style="color:#428bca">Thành phần </span>: nếu hàng hóa này có thành phần</li>
                <li><span style="color:#428bca">Đơn vị tính </span>: các đơn vị của sản phẩm ví dụ: Két, lốc, chai, tấn, kg, ... Một hàng hóa có thể có nhiều đơn vị với các hệ số quy đổi khác nhau ví dụ: 1 lốc = 6 lon</li>
                <li><span style="color:#428bca">Thông tin mở rộng </span>: bao gồm Hình ảnh và Mô tả</li> 
			</ol>   
        <p>Xong mọi việc bạn nhấn nút "Lưu" để lưu thêm hàng hóa</p>    
        <h6><strong>1.3.2 Thêm dịch vụ</strong></h6>
        <p>
            Các Thông tin dịch vụ bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Mã hàng hóa </span>, <span style="color:#428bca">Tên hàng hóa </span>, 
                    <span style="color:#428bca">Nhóm hàng </span>, <span style="color:#428bca">Giá bán </span>,
                    <span style="color:#428bca">Giá vốn </span>: Đã được nêu ở phần thêm hàng hóa ở trên</li>
                 
                <li><span style="color:#428bca">Lựa chọn khác </span>: "Được bán trực tiếp": sản phẩm sẽ xuất hiện trên màn hình bán hàng ở chức năng <a href="/tinh-nang.php?t=thungan">Thu ngân</a></li>
			</ol>
        <p>
            Ở phần thông tin mở rộng chỉ có 1 mục là :
            </p><ol>				 
                <li><span style="color:#428bca">Thông tin mở rộng </span>: bao gồm Hình ảnh và Mô tả</li> 
			</ol>      
        <p>Xong mọi việc bạn nhấn nút "Lưu" để lưu thêm dịch vụ</p>
        <h6><strong>1.3.3 Thêm combo-đóng gói</strong></h6>
        <p>
            Các Thông tin combo-đóng gói bao gồm :
            </p><ol>
				<li><span style="color:#428bca">Mã hàng hóa </span>, <span style="color:#428bca">Tên hàng hóa </span>, 
                    <span style="color:#428bca">Nhóm hàng </span>, <span style="color:#428bca">Giá bán </span>: Đã được nêu ở phần thêm hàng hóa ở trên</li>
                 
                <li><span style="color:#428bca">Lựa chọn khác </span>: "Được bán trực tiếp": sản phẩm sẽ xuất hiện trên màn hình bán hàng ở chức năng <a href="/tinh-nang.php?t=thungan">Thu ngân</a></li>
                <li><span style="color:#428bca">Thành phần </span></li>
                
			</ol>
        <p>
            Ở phần thông tin mở rộng chỉ có 1 mục là :
            </p><ol>				 
                <li><span style="color:#428bca">Thông tin mở rộng </span>: bao gồm Hình ảnh và Mô tả</li> 
			</ol>      
        <p>Xong mọi việc bạn nhấn nút "Lưu" để lưu thêm combo-dịch vụ</p>
        <h5><strong>1.4. Sửa và xóa hàng hóa</strong></h5>
        <p style="text-align: justify;">Ở danh sách hàng hóa bạn nhấn vào 1 dòng sản phẩm sẽ hiển thị chi tiết sản phẩm đó, 
        để sửa sản phẩm đó bạn nhấn nút "Cập nhật" màu xanh, để xóa sản phẩm này thì nhấn nút "Xóa" màu đỏ,
        để ngừng/cho phép kinh doanh bạn nhấn nút màu đỏ tương ứng</p>
        <h5><strong>1.4. Chuyển đổi đơn vị</strong></h5>
        <p style="text-align: justify;">Khi hàng hóa có nhiều đơn vị như gói, cây ... bạn có thể chuyển đổi số lượng qua lại giữa các đơn vị này, ví dụ:
        Chuyển 1 cây Thuốc lá thành 10 gói thuốc lá, hay 1 vỉ thuốc thành 10 viên thuốc để bán lẻ</p>
        <p style="text-align: justify;">Để sử dụng chức năng này bạn vào xem chi tiết sản phẩm (với sản phẩm có nhiều đơn vị) sẽ thấy tab "chuyển đổi", bạn nhấn vào tab "chuyển đổi" này sẽ vào được chức năng này</p>
        <p style="text-align: center;"><img class="aligncenter size-full wp-image-9233" src="/Images/danh_sach_hop_dong_cam_do.png" alt="Danh mục hàng hóa"></p>
        
         
        <h4><strong>2. Chức năng Kiểm kho</strong></h4>
        <p style="text-align: justify;">Chức năng này có thể sửa đổi số lượng các sản phẩm trong kho cho phù hợp với thực tế</p>
        <p style="text-align: justify;">Để sử dụng chức năng này bạn vào menu "Hàng hóa" ở menu chính, rồi bạn click vào submenu "Kiểm kho" để có thể thực hiện việc kiểm kho</p>
        <p style="text-align: justify;">Tại đây bạn có thể dễ dàng kiểm kho, thấy được lịch sử kiểm kho</p>
        <p style="text-align: justify;">Khi vào màn hình chính của quản lý chi nhánh bạn sẽ thấy danh sách các phiếu kiểm kho, 
        ấn vào 1 dòng sẽ hiển thị thông tin chi tiết của phiếu kiểm kho đó.</p>
        
        <p style="text-align: justify;">Các thông tin phiếu kiểm kho bạn có thể sửa được là: <span style="color:#428bca">Thời gian </span>,<span style="color:#428bca">Người kiểm </span> và <span style="color:#428bca">Ghi chú </span></p>
        <p style="text-align: justify;">Để sửa phiếu kiểm kho bạn ấn nút "Cập nhật".</p>
        <p style="text-align: justify;">Để tạo thêm phiếu kiểm kho bạn ấn nút "Kiểm kho" màu xanh.</p>
        <p style="text-align: justify;">Để hủy bỏ phiếu kiểm kho bạn ấn nút "Hủy bỏ" màu đỏ.</p>
        
        <h5><strong>2.1. Tạo phiếu kiểm kho</strong></h5>
        <p style="text-align: justify;">Ở phía trên có ô tìm kiếm hàng hóa theo tên hoặc mã sản phẩm, bạn có thể dùng máy quét mã vạch tại ô này</p>
        <p style="text-align: justify;">Tại ô này có nút "Chọn nhóm hàng", bạn có thể ấn vào đó rồi chọn nhóm hàng nào đó, xong ấn nút "Xong" màu xanh thì tất cả các sản phẩm của nhóm hàng đó sẽ được đưa vào phiếu kiểm</p>
        <p style="text-align: justify;">Ở phía bên phải có 3 tab:</p>
        <ol>
				<li><span style="color:#428bca">Thông tin </span>: Bao gồm trạng thái và mã phiếu kiểm (có thể bỏ trống)</li>
                 
                <li><span style="color:#428bca">Mở rộng </span>: Bao gồm người tạo và ngày tạo</li>
                <li><span style="color:#428bca">Ghi chú </span></li>
                
			</ol>
